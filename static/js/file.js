function f1() {
    if (document.getElementById("n_account").style.display == 'none' && document.getElementById("e_account").style.display == 'none')
    {
        document.getElementById("n_account").style.display = 'block';
        document.getElementById("e_account").style.display = 'block';

    }
    else
    {
        document.getElementById("n_account").style.display = 'none';
        document.getElementById("e_account").style.display = 'none';

    }
}
